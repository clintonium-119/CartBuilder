<h2>Installation Instructions</h2>
<p>
This game consists of a POP file and supporting sound assets packaged in a .zip file. Please expand the contents into a directory on you computer - you will see a POP file and a sub-directory called music. Please ensure both the .POP file and folder are copied to the root directory of your SD card.
</p><p>
Depending on what other games are installed on your Pokitto, you may already have a sub-directory called /music on your machine. If prompted, simply merge the contents of the Trials of Astarok directory with the folder already on your machine. The names of the file are unique and should not clash!</p>
