<h1>Road Trip</h1>

<p>
Source &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a href="https://github.com/Press-Play-On-Tape/RoadTrip" target="_new">GitHub Source</a><br>
Hex &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<a href="https://github.com/Press-Play-On-Tape/RoadTrip/releases/download/V1.0/RoadTrip.hex" target="_new">Hex File</a><br>
</p>
<p>
Inspired by the Atari 2600 game Enduro.
</p>
<p>
Strap on your goggles. Sink into your seat. And leave all your fears in the pit. You’re about to enter the race of your life. You’ll be required to pass lots of cars each day. From sunrise to sunset - as fast as you can. Welcome to the National Enduro!
</p>
<p>
With each passing day, your target number of cars will increase. For an extra challenge, switch to a manual transmission!
</p>
<p>
Special thanks to @drummyfish for some sample code that inspired the whole game and for answering questions along the way. Thanks also to <a hred="https://community.arduboy.com/u/acedent/summary" target="_new">@acedent</a> for taking the time to play the game and give some valuable feedback along the way.
</p>
<p>
Scores can be reset by pressing and holding the <b>Left</b> and <b>Right</b> buttons for a few seconds. Bootloader press the <b>Up</b> and <b>Down</b> buttons for a few seconds to drop to bootloader.
</p>
</p>
